
public class Zoga {
	private double	x;	
	private double	y;
	private int	polmer;
	private int hitrost;
	private int smerX;
	private int smerY;
	
	public Zoga(double x,double y,  int polmer, int hitrost) {
		this.x = x;
		this.y = y;
		this.polmer = polmer;
		this.hitrost = hitrost;
	
	}

	public double getX() {
		return x;
	}

	public double getY() {
		return y;
	}

	public int getPolmer() {
		return polmer;
	}

	public int getHitrost() {
		return hitrost;
	}

	public int getSmerX() {
		return smerX;
	}

	public int getSmerY() {
		return smerY;
	}

}
